package com.sci;

public class RelationalOpt {

	public static void main(String[] args) {
		int i = 10;
		int j = 20;
		System.out.println("10 > 20 : " + (i > j));
		System.out.println("10 < 20 : " + (i < j));
		System.out.println("10 >= 20 : " + (i >= j));
		System.out.println("10 <= 20 : " + (i <= j));
		System.out.println("10 == 20 : " + (i == j));
		System.out.println("10 != 20 : " + (i != j));
	}
}
